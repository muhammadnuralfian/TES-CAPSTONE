    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked
         int baris = jTable1.getSelectedRow();
    if (baris != -1) {
        selectedIdProgram = Integer.parseInt(jTable1.getValueAt(baris, 0).toString());
        txtNamaProgram.setText(jTable1.getValueAt(baris, 1).toString());
        txtDeskripsi.setText(jTable1.getValueAt(baris, 2).toString());
        jDateChooser1.setDate(java.sql.Date.valueOf(jTable1.getValueAt(baris, 3).toString()));
        jDateChooser2.setDate(java.sql.Date.valueOf(jTable1.getValueAt(baris, 4).toString()));
        jComboBox1.setSelectedItem(jTable1.getValueAt(baris, 5).toString());
    }
    }//GEN-LAST:event_jLabel1MouseClicked


    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
    String username = JOptionPane.showInputDialog(this, "Username:");
        if (username == null || username.isBlank()) return;

        String password = JOptionPane.showInputDialog(this, "Password:");
        if (password == null || password.isBlank()) return;

        String alamat = JOptionPane.showInputDialog(this, "Alamat (boleh kosong):");
        if (alamat == null) alamat = "";

        String kontak = JOptionPane.showInputDialog(this, "Kontak (boleh kosong):");
        if (kontak == null) kontak = "";

        Stakeholder s = new Stakeholder(username, password, alamat, kontak);

        try {
            boolean ok = akunDAO.tambahAkun(s);
            if (ok) {
                JOptionPane.showMessageDialog(this, "Stakeholder berhasil ditambahkan!");
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menambah stakeholder!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            // kalau username unik dan sudah ada, MySQL biasanya lempar SQLIntegrityConstraintViolationException
            JOptionPane.showMessageDialog(this, "Gagal menambah: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin diedit!");
            return;
        }

        int id = (int) model.getValueAt(row, 0);
        String curUsername = (String) model.getValueAt(row, 1);
        String curPassword = (String) model.getValueAt(row, 2);
        String curAlamat = (String) model.getValueAt(row, 3);
        String curKontak = (String) model.getValueAt(row, 4);

        String username = JOptionPane.showInputDialog(this, "Username:", curUsername);
        if (username == null || username.isBlank()) return;

        String password = JOptionPane.showInputDialog(this, "Password:", curPassword);
        if (password == null || password.isBlank()) return;

        String alamat = JOptionPane.showInputDialog(this, "Alamat:", curAlamat);
        if (alamat == null) alamat = "";

        String kontak = JOptionPane.showInputDialog(this, "Kontak:", curKontak);
        if (kontak == null) kontak = "";

        Stakeholder s = new Stakeholder(username, password, alamat, kontak);
        s.setIdAkun(id);

        if (akunDAO.updateAkun(s)) {
            JOptionPane.showMessageDialog(this, "Stakeholder berhasil diperbarui!");
            loadData();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui stakeholder!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus!");
            return;
        }

        int id = (int) model.getValueAt(row, 0);
        int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            if (akunDAO.hapusAkun(id)) {
                JOptionPane.showMessageDialog(this, "Data stakeholder berhasil dihapus!");
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed


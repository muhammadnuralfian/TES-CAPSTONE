    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
         try {
        java.util.Date tanggal = jDateChooser1.getDate();
        if (tanggal == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Silakan pilih tanggal laporan!");
            return;
        }

        String lokasi = txtLokasi.getText().trim();
        String jenis = txtJenisMasalah.getText().trim();
        String deskripsi = txtDeskripsi.getText().trim();

        if (lokasi.isEmpty() || jenis.isEmpty() || deskripsi.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Semua field wajib diisi!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        int idAkun = (akun != null) ? akun.getIdAkun() : 1;

        Laporan laporan = new Laporan(
            tanggal,
            lokasi,
            jenis,
            deskripsi,
            "Menunggu Konfirmasi",
            idAkun,
            0
        );
        laporan.setIdMasyarakat(idAkun);

        if (laporanDAO.tambahLaporan(laporan)) {
            javax.swing.JOptionPane.showMessageDialog(this, "‚úÖ Laporan berhasil dikirim!");
            loadData();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "‚ùå Gagal menambah laporan!");
        }

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
    }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin diubah!");
        return;
    }

    try {
        // Ambil laporan yang dipilih
        Laporan laporan = laporanDAO.getAllLaporan().get(selectedRow);

        // Tampilkan pop-up edit
        EditLaporanDialog dialog = new EditLaporanDialog(this, laporan);
        dialog.setVisible(true);

        // Jika user menekan "Simpan"
        if (dialog.isSaved()) {
            if (laporanDAO.updateLaporan(laporan)) {
                JOptionPane.showMessageDialog(this, "‚úÖ Laporan berhasil diubah!");
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "‚ùå Gagal mengubah laporan!");
            }
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
    }

    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus!");
        return;
    }

    int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
            "Yakin ingin menghapus laporan ini?", "Konfirmasi",
            javax.swing.JOptionPane.YES_NO_OPTION);

    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        int idLaporan = laporanDAO.getAllLaporan().get(selectedRow).getIdPelapor();
        if (laporanDAO.hapusLaporan(idLaporan)) {
            javax.swing.JOptionPane.showMessageDialog(this, "üóëÔ∏è Laporan berhasil dihapus!");
            loadData();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "‚ùå Gagal menghapus laporan!");
        }
    }
    }//GEN-LAST:event_btnHapusActionPerformed


    private void loadDataProgram() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);
    for (ProgramKolaborasi p : programDAO.getAllPrograms()) {
        model.addRow(new Object[]{
            p.getIdProgram(),
            p.getNamaProgram(),
            p.getDeskripsi(),
            p.getTanggalMulai(),
            p.getTanggalSelesai(),
            p.getStatusProgram()
        });
    }
}




    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
     String username = JOptionPane.showInputDialog(this, "Masukkan Username:");
    if (username == null || username.isEmpty()) return;

    String password = JOptionPane.showInputDialog(this, "Masukkan Password:");
    if (password == null || password.isEmpty()) return;

    String[] roles = {"masyarakat", "stakeholder"};
String role = (String) JOptionPane.showInputDialog(
    this,
    "Pilih Role Pengguna:",
    "Role",
    JOptionPane.QUESTION_MESSAGE,
    null,
    roles,
    roles[0]
);
if (role == null) return;


    String alamat = JOptionPane.showInputDialog(this, "Masukkan Alamat:");
    if (alamat == null || alamat.isEmpty()) return;

    String kontak = JOptionPane.showInputDialog(this, "Masukkan Kontak:");
    if (kontak == null || kontak.isEmpty()) return;

    AkunPengguna akun;
    switch (role.toLowerCase()) {
    case "admin" -> {
        Admin admin = new Admin(username, password);
        admin.setAlamat(alamat);
        admin.setKontak(kontak);
        admin.setJabatan("Petugas");
        admin.setStatusAdmin("Aktif");
        admin.setWewenang("Penuh");
        akun = admin;
    }
    case "masyarakat" -> {
        Masyarakat m = new Masyarakat(username, password, alamat);
        m.setKontak(kontak);
        akun = m;
    }
    case "stakeholder" -> {
        Stakeholder s = new Stakeholder(username, password, alamat, kontak);
        akun = s;
    }
    default -> {
        JOptionPane.showMessageDialog(this, "Role tidak valid! Gunakan: admin / masyarakat / stakeholder");
        return;
    }
}


    if (akunDAO.tambahAkun(akun)) {
        JOptionPane.showMessageDialog(this, "‚úÖ Akun berhasil ditambahkan!");
        loadData();
    } else {
        JOptionPane.showMessageDialog(this, "‚ùå Gagal menambah akun!", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
         int row = jTable2.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih akun yang ingin diedit terlebih dahulu!");
        return;
    }

    int id = (int) model.getValueAt(row, 0);
    String currentUsername = (String) model.getValueAt(row, 1);
    String currentPassword = (String) model.getValueAt(row, 2);
    String currentRole = (String) model.getValueAt(row, 3);
    String currentAlamat = (String) model.getValueAt(row, 4);
    String currentKontak = (String) model.getValueAt(row, 5);

    String username = JOptionPane.showInputDialog(this, "Edit Username:", currentUsername);
    if (username == null || username.isEmpty()) return;

    String password = JOptionPane.showInputDialog(this, "Edit Password:", currentPassword);
    if (password == null || password.isEmpty()) return;

    // üîπ Ganti input role manual jadi JComboBox
    String[] opsiRole = {"masyarakat", "stakeholder"};
    JComboBox<String> comboRole = new JComboBox<>(opsiRole);
    comboRole.setSelectedItem(currentRole);

    int hasil = JOptionPane.showConfirmDialog(
        this,
        comboRole,
        "Pilih Role Pengguna:",
        JOptionPane.OK_CANCEL_OPTION,
        JOptionPane.QUESTION_MESSAGE
    );
    if (hasil != JOptionPane.OK_OPTION) return;
    String roleDipilih = (String) comboRole.getSelectedItem();

    String alamat = JOptionPane.showInputDialog(this, "Edit Alamat:", currentAlamat);
    if (alamat == null || alamat.isEmpty()) return;

    String kontak = JOptionPane.showInputDialog(this, "Edit Kontak:", currentKontak);
    if (kontak == null || kontak.isEmpty()) return;

    // üîπ Buat objek akun baru sesuai role
    AkunPengguna akun;
    switch (roleDipilih.toLowerCase()) {
        case "masyarakat" -> akun = new Masyarakat(username, password, alamat);
        case "stakeholder" -> akun = new Stakeholder(username, password, alamat, kontak);
        default -> {
            JOptionPane.showMessageDialog(this, "Role tidak valid!");
            return;
        }
    }

    akun.setIdAkun(id);
    akun.setAlamat(alamat);
    akun.setKontak(kontak);

    // üîπ Simpan ke database
    if (akunDAO.updateAkun(akun)) {
        JOptionPane.showMessageDialog(this, "Data akun berhasil diperbarui!");
        loadData();
    } else {
        JOptionPane.showMessageDialog(this, "Gagal memperbarui akun!", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnEditActionPerformed
    
    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int row = jTable2.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih akun yang ingin dihapus!");
            return;
        }
        int id = (int) model.getValueAt(row, 0);
        if (akunDAO.hapusAkun(id)) {
            JOptionPane.showMessageDialog(this, "Akun berhasil dihapus!");
            loadData();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menghapus akun!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnHapusActionPerformed


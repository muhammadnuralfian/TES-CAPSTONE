public void setJenisStakeholderSelected(String jenis) {
    if (jenis == null) return;
    for (int i = 0; i < jComboBox1.getItemCount(); i++) {
        if (jComboBox1.getItemAt(i).equalsIgnoreCase(jenis)) {
            jComboBox1.setSelectedIndex(i);
            break;
        }
    }
}



   private void loadData() {
    model.setRowCount(0);
    // kalau kamu sudah buat getAllStakeholder(), pakai itu; kalau belum, pakai getAllAkun() lalu filter di sini
    for (AkunPengguna akun : akunDAO.getAllAkun()) {
        if (!"stakeholder".equalsIgnoreCase(akun.getRole())) continue;
        model.addRow(new Object[] {
            akun.getIdAkun(),
            akun.getUsername(),
            akun.getPassword(),
            akun.getRole(),
            akun.getAlamat(),
            akun.getKontak()
        });
    }
}

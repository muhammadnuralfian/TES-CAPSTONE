    private void loadData() {
    if (model == null) {
        System.err.println("‚ö†Ô∏è Model tabel belum diinisialisasi!");
        return;
    }

    model.setRowCount(0); // hapus isi tabel lama

    for (Laporan l : laporanDAO.getAllLaporan()) {
        model.addRow(new Object[]{
            l.getTanggalLaporan(),
            l.getLokasiLaporan(),
            l.getJenisMasalah(),
            l.getDeskripsi(),
            l.getStatusLaporan()
        });
    }
}



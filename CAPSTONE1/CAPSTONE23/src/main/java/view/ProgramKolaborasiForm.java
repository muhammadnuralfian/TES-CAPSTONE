    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        try {
        if (txtNamaProgram.getText().isEmpty() || txtDeskripsi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua data harus diisi!");
            return;
        }

        ProgramKolaborasi p = new ProgramKolaborasi();
        p.setNamaProgram(txtNamaProgram.getText());
        p.setDeskripsi(txtDeskripsi.getText());
        p.setTanggalMulai(new java.sql.Date(jDateChooser1.getDate().getTime()));
        p.setTanggalSelesai(new java.sql.Date(jDateChooser2.getDate().getTime()));
        p.setStatusProgram((String) jComboBox1.getSelectedItem());

        ProgramKolaborasiDAO dao = new ProgramKolaborasiDAOImpl();
        if (dao.addProgram(p)) {
            JOptionPane.showMessageDialog(this, "✅ Program berhasil ditambahkan!");
            loadDataProgram();
        } else {
            JOptionPane.showMessageDialog(this, "❌ Gagal menambahkan program!");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
          if (selectedIdProgram == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu!");
        return;
    }

    ProgramKolaborasi p = new ProgramKolaborasi();
    p.setIdProgram(selectedIdProgram);
    p.setNamaProgram(txtNamaProgram.getText());
    p.setDeskripsi(txtDeskripsi.getText());
    p.setTanggalMulai(new java.sql.Date(jDateChooser1.getDate().getTime()));
    p.setTanggalSelesai(new java.sql.Date(jDateChooser2.getDate().getTime()));
    p.setStatusProgram(jComboBox1.getSelectedItem().toString());

    ProgramKolaborasiDAO dao = new ProgramKolaborasiDAOImpl();
    if (dao.updateProgram(p)) {
        JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
        loadDataProgram();
    } else {
        JOptionPane.showMessageDialog(this, "Gagal memperbarui data!");
    }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
         if (selectedIdProgram == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus!");
        return;
    }

    ProgramKolaborasiDAO dao = new ProgramKolaborasiDAOImpl();
    if (dao.deleteProgram(selectedIdProgram)) {
        JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
        loadDataProgram();
    } else {
        JOptionPane.showMessageDialog(this, "Gagal menghapus data!");
    }
    }//GEN-LAST:event_btnHapusActionPerformed

